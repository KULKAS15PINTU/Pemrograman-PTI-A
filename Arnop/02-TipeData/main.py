# integer
# Tipe Data : bilangan bulat tanpa koma
data_integer = 11
print("data =", data_integer)
print("type data =", type(data_integer))

# float
# Tipe data : bilangan desimal, dengan koma
data_float = 3.14
print("data =", data_float)
print("type data =", type(data_float))

# string
# Tipe Data : kumpulan karakter
data_string = "Universitas Pendidikan Mandalika"
print("data =", data_string)
print("type data =", type(data_string))

# boolean
# tipe data : logika TRUE / FALSE
data_bool = True
print("data =", data_bool)
print("type data =", type(data_bool))